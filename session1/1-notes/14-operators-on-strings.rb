# You can use basic operators on Strings, just like other objects:

my_string   = "Face"
your_string = "book"

my_string + your_string  # => "Facebook" # CONCATENATE

my_string == your_string # => false # SAY IS EQUAL TO, or is the equality operator
my_string = "book" # SAY EQUALS, assignment operator
my_string == your_string # => true

