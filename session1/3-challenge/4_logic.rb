def grade(num_books, has_read_books)

	if has_read_books
		return "C" if num_books < 10
		return "B" if num_books <=20
		return "A"
	else
		return "D" if num_books < 10
		return "C" if num_books <= 20
		return "B"
	
	end

end
	
	
	
		
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	